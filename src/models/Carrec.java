package models;

public class Carrec {
	private final String masculi;
	private final String femeni;

	public Carrec(String masculi, String femeni) {
		this.masculi = masculi;
		this.femeni = femeni;
	}

	public String getMasculi() {
		return masculi;
	}

	public String getFemeni() {
		return femeni;
	}
}
